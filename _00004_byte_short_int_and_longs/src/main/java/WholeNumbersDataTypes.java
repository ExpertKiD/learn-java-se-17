public class WholeNumbersDataTypes {

    public static void main(String[] args) {
        System.out.println("Byte Data Type:: " +
                "Min Value = " + Byte.MIN_VALUE + " "
                + "Max Value = " + Byte.MAX_VALUE + " "
                + " Width = " + Byte.SIZE);

        System.out.println("Short Data Type:: " +
                "Min Value = " + Short.MIN_VALUE + " "
                + "Max Value = " + Short.MAX_VALUE + " "
                + " Width = " + Short.SIZE);

        System.out.println("Integer Data Type:: " +
                "Min Value = " + Integer.MIN_VALUE + " "
                + "Max Value = " + Integer.MAX_VALUE + " "
                + " Width = " + Integer.SIZE);

        System.out.println("Long Data Type:: " +
                "Min Value = " + Long.MIN_VALUE + " "
                + "Max Value = " + Long.MAX_VALUE + " "
                + " Width = " + Long.SIZE);
    }
}
